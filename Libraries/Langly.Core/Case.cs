namespace Langly {
	/// <summary>
	/// Text case comparison mode.
	/// </summary>
	public enum Case {
		/// <summary>
		/// Comparisons should be done without regards to letter casing.
		/// </summary>
		Insensitive = 0,

		/// <summary>
		/// Comparisons should be done with regards to letter casing.
		/// </summary>
		Sensitive = 1,
	}
}
