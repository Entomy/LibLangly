		Shaw = 281,

		/// <summary>
		/// Emoji
		/// </summary>
		Zsye = 993,

		/// <summary>
		/// Code for inherited script
		/// </summary>
		Zinh = 994,

		/// <summary>
		/// Mathematical notation
		/// </summary>
		Zmth = 995,

		/// <summary>
		/// Symbols
		/// </summary>
		Zsym = 996,

		/// <summary>
		/// Code for unwritten documents
		/// </summary>
		Zxxx = 997,

		/// <summary>
		/// Code for undetermined script
		/// </summary>
		Zyyy = 998,

		/// <summary>
		/// Code for uncoded script
		/// </summary>
		Zzzz = 999,
	}
}
