namespace Langly.Streams {
	/// <summary>
	/// Represents the kind of standard stream.
	/// </summary>
	public enum Standard {
		/// <summary>
		/// The input stream; the keyboard.
		/// </summary>
		Input,

		/// <summary>
		/// The output stream; the console.
		/// </summary>
		Output,

		/// <summary>
		/// The error stream.
		/// </summary>
		Error,
	}
}
